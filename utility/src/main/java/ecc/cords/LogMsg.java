package ecc.cords;

public class LogMsg {

	private String logMsg;
	private String color;

	public LogMsg(String logMsg, String color) {
		this.logMsg = logMsg;
		this.color = color;
	}

	public String getLogMsg() {
		return logMsg;
	}

	public void setLogMsg(String logMsg) {
		this.logMsg = logMsg;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String logMsg) {
		this.color = color;
	}
}